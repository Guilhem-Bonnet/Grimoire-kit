class TestApplyProjectContext:
    def test_updates_all_user_fields(self, project_tree: Path):
        cfg = UserConfig("new-proj", "NewUser", "English", "English", "beginner")
        path = project_tree / "project-context.yaml"
        assert grimoire_setup.apply_project_context(path, cfg) is True
        text = path.read_text(encoding="utf-8")
        assert 'name: "new-proj"' in text
        assert 'name: "NewUser"' in text
        assert 'language: "English"' in text
        assert 'document_language: "English"' in text
        assert 'skill_level: "beginner"' in text

    def test_preserves_other_sections(self, project_tree: Path):
        cfg = UserConfig("new-proj", "NewUser", "EN", "EN", "expert")
        path = project_tree / "project-context.yaml"
        grimoire_setup.apply_project_context(path, cfg)
        text = path.read_text(encoding="utf-8")
        assert "stack:" in text
        assert "memory:" in text
        assert "backend:" in text

    def test_no_change_returns_false(self, project_tree: Path):
        cfg = UserConfig("my-project", "Alice", "English", "English", "intermediate")
        path = project_tree / "project-context.yaml"
        assert grimoire_setup.apply_project_context(path, cfg) is False

    def test_distinguishes_project_name_from_user_name(self, project_tree: Path):
        cfg = UserConfig("ProjectX", "UserY", "FR", "FR", "expert")
        path = project_tree / "project-context.yaml"
        grimoire_setup.apply_project_context(path, cfg)
        text = path.read_text(encoding="utf-8")
        lines = text.splitlines()
        # Find the project.name and user.name lines
        proj_name_line = None
        user_name_line = None
        section = ""
        for line in lines:
            s = line.strip()
            if s and not s.startswith("#") and not line[0:1].isspace() and ":" in s:
                section = s.split(":")[0].strip()
            if section == "project" and s.startswith("name:"):
                proj_name_line = s
            elif section == "user" and s.startswith("name:"):
                user_name_line = s
        assert proj_name_line == 'name: "ProjectX"'
        assert user_name_line == 'name: "UserY"'


