# ── Tests: CLI main() ────────────────────────────────────────────────────────


class TestMainCLI:
    def test_check_returns_1_on_desync(self, project_tree: Path):
        rc = grimoire_setup.main(["--project-root", str(project_tree), "--check"])
        assert rc == 1

    def test_check_returns_0_on_sync(self, synced_tree: Path):
        rc = grimoire_setup.main(["--project-root", str(synced_tree), "--check"])
        assert rc == 0

    def test_check_json_output(self, project_tree: Path, capsys):
        grimoire_setup.main(["--project-root", str(project_tree), "--check", "--json"])
        out = capsys.readouterr().out
        data = json.loads(out)
        assert data["synced"] is False
        assert len(data["diffs"]) > 0

    def test_sync_mode(self, project_tree: Path):
        rc = grimoire_setup.main(["--project-root", str(project_tree), "--sync"])
        assert rc == 0
        # Verify sync happened
        rc2 = grimoire_setup.main(["--project-root", str(project_tree), "--check"])
        assert rc2 == 0

    def test_cli_overrides(self, project_tree: Path):
        rc = grimoire_setup.main([
            "--project-root", str(project_tree),
            "--user", "NewUser",
            "--lang", "Deutsch",
        ])
        assert rc == 0
        cfg = grimoire_setup.load_project_context(project_tree / "project-context.yaml")
        assert cfg.user_name == "NewUser"
        assert cfg.communication_language == "Deutsch"
        # Unchanged fields kept from original
        assert cfg.project_name == "my-project"

    def test_missing_project_context(self, tmp_path: Path):
        rc = grimoire_setup.main(["--project-root", str(tmp_path), "--check"])
        assert rc == 1

    def test_missing_project_context_json(self, tmp_path: Path, capsys):
        rc = grimoire_setup.main(["--project-root", str(tmp_path), "--check", "--json"])
        assert rc == 1
        out = capsys.readouterr().out
        data = json.loads(out)
        assert "error" in data

    def test_sync_json_output(self, project_tree: Path, capsys):
        grimoire_setup.main(["--project-root", str(project_tree), "--sync", "--json"])
        out = capsys.readouterr().out
        data = json.loads(out)
        assert data["synced"] is True
        assert len(data["updated"]) > 0

    def test_sync_idempotent(self, project_tree: Path):
        grimoire_setup.main(["--project-root", str(project_tree), "--sync"])
        rc = grimoire_setup.main(["--project-root", str(project_tree), "--check"])
        assert rc == 0


