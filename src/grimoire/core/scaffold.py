# ── Scaffolder ────────────────────────────────────────────────────────────────


class ProjectScaffolder:
    """Build and execute a scaffold plan for a Grimoire project."""

    def __init__(
        self,
        target: Path,
        *,
        project_name: str,
        user_name: str,
        language: str,
        skill_level: str,
        scan: ScanResult | None,
        resolved: ResolvedArchetype,
        backend: str,
    ) -> None:
        self._target = target.resolve()
        self._project_name = project_name
        self._user_name = user_name
        self._language = language
        self._skill_level = skill_level
        self._scan = scan
        self._resolved = resolved
        self._backend = backend
        self._archetypes = archetypes_path()
        self._framework = framework_path()

    # ── Public API ────────────────────────────────────────────────────

    def plan(self) -> ScaffoldPlan:
        """Build a complete scaffold plan without touching the filesystem."""
        p = ScaffoldPlan()
        self._plan_directories(p)
        self._plan_meta_agents(p)
        self._plan_archetype_agents(p)
        self._plan_stack_agents(p)
        self._plan_feature_agents(p)
        self._plan_framework(p)
        self._plan_templates(p)
        self._plan_agent_wrappers(p)
        self._plan_copilot_instructions(p)
        self._plan_gitignore(p)
        return p

    def execute(self, plan: ScaffoldPlan) -> ScaffoldResult:
        """Execute a scaffold plan, writing to the filesystem."""
        result = ScaffoldResult()

        for d in plan.directories:
            d.mkdir(parents=True, exist_ok=True)
            result.created_dirs.append(str(d.relative_to(self._target)))

        for fc in plan.copies:
            fc.dst.parent.mkdir(parents=True, exist_ok=True)
            if fc.src.is_dir():
                shutil.copytree(fc.src, fc.dst, dirs_exist_ok=True)
            else:
                shutil.copy2(fc.src, fc.dst)
            result.copied_files.append(fc.label or str(fc.dst.relative_to(self._target)))

        for tr in plan.templates:
            tr.dst.parent.mkdir(parents=True, exist_ok=True)
            tr.dst.write_text(tr.content, encoding="utf-8")
            result.rendered_files.append(tr.label or str(tr.dst.relative_to(self._target)))

        return result

    # ── Planning helpers ──────────────────────────────────────────────

    def _agents_dir(self) -> Path:
        return self._target / "_grimoire" / "_config" / "custom" / "agents"

    def _memory_dir(self) -> Path:
        return self._target / "_grimoire" / "_memory"

    def _tpl_vars(self) -> dict[str, str]:
        stacks = [d.name for d in self._scan.stacks] if self._scan else []
        stack_list = ", ".join(f'"{s}"' for s in stacks) if stacks else ""
        archetypes = self._resolved.archetypes or (self._resolved.archetype,)
        archetype_list = ", ".join(f'"{a}"' for a in archetypes)
        return {
            "project_name": self._project_name,
            "user_name": self._user_name,
            "language": self._language,
            "skill_level": self._skill_level,
            "project_type": self._scan.project_type if self._scan else "generic",
            "stack_list": stack_list,
            "archetype": ", ".join(archetypes),
            "archetype_list": archetype_list,
            "backend": self._backend,
        }

    def _plan_directories(self, p: ScaffoldPlan) -> None:
        base = self._target
        dirs = [
            base / ".github" / "agents",
            base / "_grimoire" / "_config" / "custom" / "agents",
            base / "_grimoire" / "_config" / "custom" / "prompt-templates",
            base / "_grimoire" / "_config" / "custom" / "workflows",
            base / "_grimoire" / "_memory" / "agent-learnings",
            base / "_grimoire" / "_memory" / "session-summaries",
            base / "_grimoire" / "_memory" / "archives",
            base / "_grimoire-output" / ".runs" / "main",
            base / "_grimoire-output" / "team-vision",
            base / "_grimoire-output" / "team-build" / "stories",
            base / "_grimoire-output" / "team-build" / "test-reports",
            base / "_grimoire-output" / "team-ops",
            base / "_grimoire-output" / "contracts",
        ]
        p.directories.extend(dirs)

    def _plan_meta_agents(self, p: ScaffoldPlan) -> None:
        meta_dir = self._archetypes / "meta" / "agents"
        if not meta_dir.is_dir():
            return
        agents_dst = self._agents_dir()
        for md in sorted(meta_dir.glob("*.md")):
            p.copies.append(FileCopy(
                src=md,
                dst=agents_dst / md.name,
                label=f"meta/{md.stem}",
            ))

    def _plan_archetype_agents(self, p: ScaffoldPlan) -> None:
        archetypes = self._resolved.archetypes or (self._resolved.archetype,)
        for arch in archetypes:
            arch_dir = self._archetypes / arch
            if not arch_dir.is_dir():
                continue

            # Agents
            agents_src = arch_dir / "agents"
            if agents_src.is_dir():
                agents_dst = self._agents_dir()
                for md in sorted(agents_src.glob("*.md")):
                    # Skip if already planned (from a previous archetype layer)
                    if any(fc.dst == agents_dst / md.name for fc in p.copies):
                        continue
                    p.copies.append(FileCopy(
                        src=md,
                        dst=agents_dst / md.name,
                        label=f"{arch}/{md.stem}",
                    ))

            # Archetype DNA — merge multiple into composite
            dna_src = arch_dir / "archetype.dna.yaml"
            if dna_src.is_file():
                p.copies.append(FileCopy(
                    src=dna_src,
                    dst=self._target / "_grimoire" / "_config" / f"archetype.dna.{arch}.yaml",
                    label=f"{arch}/archetype.dna.yaml",
                ))
                # Also keep a primary DNA copy for backward compat (first archetype wins)
                primary_dst = self._target / "_grimoire" / "_config" / "archetype.dna.yaml"
                if not any(fc.dst == primary_dst for fc in p.copies):
                    p.copies.append(FileCopy(
                        src=dna_src,
                        dst=primary_dst,
                        label=f"{arch}/archetype.dna.yaml",
                    ))

            # Shared context template (first archetype with template wins)
            shared_tpl = arch_dir / "shared-context.tpl.md"
            shared_dst = self._memory_dir() / "shared-context.md"
            if shared_tpl.is_file() and not any(t.dst == shared_dst for t in p.templates):
                content = shared_tpl.read_text(encoding="utf-8")
                rendered = Template(content).safe_substitute(self._tpl_vars())
                p.templates.append(TemplateRender(
                    dst=shared_dst,
                    content=rendered,
                    label="shared-context.md (archetype)",
                ))

    def _plan_stack_agents(self, p: ScaffoldPlan) -> None:
        stack_agents_dir = self._archetypes / "stack" / "agents"
        if not stack_agents_dir.is_dir():
            return
        agents_dst = self._agents_dir()
        for agent_name in self._resolved.stack_agents:
            md = stack_agents_dir / f"{agent_name}.md"
            if md.is_file():
                p.copies.append(FileCopy(
                    src=md,
                    dst=agents_dst / md.name,
                    label=f"stack/{agent_name}",
                ))

    def _plan_feature_agents(self, p: ScaffoldPlan) -> None:
        agents_dst = self._agents_dir()
        for agent_name in self._resolved.feature_agents:
            # Known feature agent paths
            if agent_name == "vectus":
                src = self._archetypes / "features" / "vector-memory" / "vectus.md"
                if src.is_file():
                    p.copies.append(FileCopy(
                        src=src,
                        dst=agents_dst / "vectus.md",
                        label="feature/vectus",
                    ))

    def _plan_framework(self, p: ScaffoldPlan) -> None:
        fw = self._framework
        dst_custom = self._target / "_grimoire" / "_config" / "custom"

        # Core framework files
        for name in ("agent-base.md", "agent-base-compact.md"):
            src = fw / name
            if src.is_file():
                p.copies.append(FileCopy(src=src, dst=dst_custom / name, label=f"framework/{name}"))

        # Shell scripts
        for name in ("cc-verify.sh", "sil-collect.sh"):
            src = fw / name
            if src.is_file():
                p.copies.append(FileCopy(src=src, dst=dst_custom / name, label=f"framework/{name}"))

        # Prompt templates
        pt_src = fw / "prompt-templates"
        if pt_src.is_dir():
            p.copies.append(FileCopy(
                src=pt_src,
                dst=dst_custom / "prompt-templates",
                label="framework/prompt-templates/",
            ))

        # Workflows
        wf_src = fw / "workflows"
        if wf_src.is_dir():
            p.copies.append(FileCopy(
                src=wf_src,
                dst=dst_custom / "workflows",
                label="framework/workflows/",
            ))

        # Memory system
        mem_dst = self._memory_dir()
        for name in ("maintenance.py", "session-save.py"):
            src = fw / "memory" / name
            if src.is_file():
                p.copies.append(FileCopy(src=src, dst=mem_dst / name, label=f"memory/{name}"))

        backends_src = fw / "memory" / "backends"
        if backends_src.is_dir():
            p.copies.append(FileCopy(
                src=backends_src,
                dst=mem_dst / "backends",
                label="memory/backends/",
            ))

        # Memory templates
        for tpl_name in ("contradiction-log.tpl.md", "failure-museum.tpl.md"):
            src = fw / "memory" / tpl_name
            if src.is_file():
                dst_name = tpl_name.replace(".tpl", "")
                p.copies.append(FileCopy(src=src, dst=mem_dst / dst_name, label=f"memory/{dst_name}"))

    def _plan_templates(self, p: ScaffoldPlan) -> None:
        v = self._tpl_vars()

        # project-context.yaml
        p.templates.append(TemplateRender(
            dst=self._target / "project-context.yaml",
            content=Template(_PROJECT_CONTEXT_TPL).safe_substitute(v),
            label="project-context.yaml",
        ))

        # Memory config
        p.templates.append(TemplateRender(
            dst=self._memory_dir() / "config.yaml",
            content=Template(_MEMORY_CONFIG_TPL).safe_substitute(v),
            label="_grimoire/_memory/config.yaml",
        ))

        # Shared context (only if archetype didn't provide one)
        shared_dst = self._memory_dir() / "shared-context.md"
        if not any(t.dst == shared_dst for t in p.templates):
            p.templates.append(TemplateRender(
                dst=shared_dst,
                content=Template(_SHARED_CONTEXT_DEFAULT).safe_substitute(v),
                label="shared-context.md (default)",
            ))

        # Decisions log
        p.templates.append(TemplateRender(
            dst=self._memory_dir() / "decisions-log.md",
            content=_DECISIONS_LOG,
            label="decisions-log.md",
        ))

        # Session state
        p.templates.append(TemplateRender(
            dst=self._memory_dir() / "session-state.md",
            content="# Session State\n\nstatus: idle\nfirst_run: true\n",
            label="session-state.md",
        ))

        # Agent manifest (enriched with category + description)
        manifest_lines = ["name,file,category,description,icon\n"]
        for fc in p.copies:
            if fc.dst.suffix != ".md" or "/agents/" not in str(fc.dst):
                continue
            name = fc.dst.stem
            category = fc.label.split("/")[0] if fc.label and "/" in fc.label else "—"
            desc = self._extract_agent_description(fc.src).replace(",", ";")
            manifest_lines.append(f"{name},{name}.md,{category},{desc},🤖\n")
        p.templates.append(TemplateRender(
            dst=self._target / "_grimoire" / "_config" / "agent-manifest.csv",
            content="".join(manifest_lines),
            label="agent-manifest.csv",
        ))

        # Session branch metadata
        p.templates.append(TemplateRender(
            dst=self._target / "_grimoire-output" / ".runs" / "main" / "branch.json",
            content='{"name": "main", "created": "auto", "active": true}\n',
            label=".runs/main/branch.json",
        ))

    def _plan_agent_wrappers(self, p: ScaffoldPlan) -> None:
        """Generate .github/agents/*.agent.md wrappers for VS Code discovery."""
        gh_agents = self._target / ".github" / "agents"
        concierge_name = "concierge"
        for fc in p.copies:
            if fc.dst.suffix != ".md" or "/agents/" not in str(fc.dst):
                continue
            name = fc.dst.stem
            wrapper_dst = gh_agents / f"{name}.agent.md"
            if wrapper_dst.is_file():
                continue
            desc = self._extract_agent_description(fc.src)
            is_entry = name == concierge_name
            tools = "['read', 'search']" if not is_entry else "['read', 'search', 'execute']"
            lines = [
                "---",
                f"description: '{desc}'",
                f"tools: {tools}",
            ]
            if not is_entry:
                lines.append("user-invocable: false")
            lines.append("---")
            lines.append("")
            lines.append(f"1. Load {{{{project-root}}}}/_grimoire/_config/custom/agents/{name}.md")
            lines.append("2. Follow ALL activation instructions in the agent file")
            lines.append("")
            p.templates.append(TemplateRender(
                dst=wrapper_dst,
                content="\n".join(lines),
                label=f".github/agents/{name}.agent.md",
            ))

    @staticmethod
    def _extract_agent_description(src: Path) -> str:
        """Extract description from internal agent YAML frontmatter."""
        try:
            text = src.read_text(encoding="utf-8")
        except OSError:
            return "Grimoire agent"
        m = re.search(r'^description:\s*["\']?(.+?)["\']?\s*$', text, re.MULTILINE)
        if m:
            return m.group(1).replace("'", "''")
        return "Grimoire agent"

    def _plan_copilot_instructions(self, p: ScaffoldPlan) -> None:
        copilot_file = self._target / ".github" / "copilot-instructions.md"
        if copilot_file.is_file():
            return  # Don't overwrite existing instructions

        # Build enriched agents table with descriptions
        lines = ["| Agent | Role | Description |", "|-------|------|-------------|",]
        for fc in p.copies:
            if fc.dst.suffix != ".md" or "/agents/" not in str(fc.dst):
                continue
            name = fc.dst.stem
            desc = self._extract_agent_description(fc.src)
            category = fc.label.split("/")[0] if fc.label and "/" in fc.label else "—"
            lines.append(f"| {name} | {category} | {desc} |")
        agents_table = "\n".join(lines) if len(lines) > 2 else "(none)"

        v = self._tpl_vars()
        v["agents_table"] = agents_table
        p.templates.append(TemplateRender(
            dst=copilot_file,
            content=Template(_COPILOT_INSTRUCTIONS_TPL).safe_substitute(v),
            label=".github/copilot-instructions.md",
        ))

    def _plan_gitignore(self, p: ScaffoldPlan) -> None:
        """Add grimoire-specific patterns to .gitignore."""
        gitignore = self._target / ".gitignore"
        marker = "# --- Grimoire Kit ---"
        section = (
            f"{marker}\n"
            "# Session runs — ephemeral runtime data\n"
            "_grimoire-output/.runs/\n"
            "# Memory archives — compressed old sessions\n"
            "_grimoire/_memory/archives/\n"
        )

        if gitignore.is_file():
            existing = gitignore.read_text(encoding="utf-8")
            if marker in existing:
                return  # Already has grimoire section
            content = existing.rstrip() + "\n\n" + section
        else:
            content = section

        p.templates.append(TemplateRender(
            dst=gitignore,
            content=content,
            label=".gitignore (grimoire patterns)",
        ))

    @staticmethod
    def _list_planned_agents(p: ScaffoldPlan) -> list[str]:
        """Extract agent labels from planned copies."""
        agents: list[str] = []
        for fc in p.copies:
            if fc.dst.suffix == ".md" and "/agents/" in str(fc.dst):
                agents.append(fc.label or fc.dst.stem)
        return agents
