"""Project scaffolding engine — creates a complete Grimoire project structure.

The engine works in two phases:

1. **Plan** — build a ``ScaffoldPlan`` describing every directory, file copy,
   and template rendering without touching the filesystem.
2. **Execute** — apply the plan atomically (as much as possible).

This separation makes ``--dry-run`` trivial and unit testing straightforward.
"""

from __future__ import annotations

import re
import shutil
from dataclasses import dataclass, field
from pathlib import Path
from string import Template

from grimoire.archetypes import bundled_path as archetypes_path
from grimoire.core.archetype_resolver import ResolvedArchetype
from grimoire.core.scanner import ScanResult
from grimoire.data import framework_path

# ── Data models ───────────────────────────────────────────────────────────────


@dataclass
class FileCopy:
    """A file to copy from *src* to *dst*."""

    src: Path
    dst: Path
    label: str = ""


@dataclass
class TemplateRender:
    """A template file to render with *variables* and write to *dst*."""

    dst: Path
    content: str
    label: str = ""


@dataclass
class ScaffoldPlan:
    """Everything the scaffolder intends to do — inspectable before execution."""

    directories: list[Path] = field(default_factory=list)
    copies: list[FileCopy] = field(default_factory=list)
    templates: list[TemplateRender] = field(default_factory=list)

    @property
    def total_operations(self) -> int:
        return len(self.directories) + len(self.copies) + len(self.templates)


@dataclass
class ScaffoldResult:
    """Outcome of executing a plan."""

    created_dirs: list[str] = field(default_factory=list)
    copied_files: list[str] = field(default_factory=list)
    rendered_files: list[str] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)

    @property
    def total(self) -> int:
        return len(self.created_dirs) + len(self.copied_files) + len(self.rendered_files)


